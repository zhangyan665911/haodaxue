$(function(){
	//搜索
	$(".globalSearchCourse").click(function(){
		reloadCourse();
	});
	
	//搜索
	$("#globalKeyword").on('keydown',function(event) {
		if(event.keyCode == 13) {
			reloadCourse();
		}
	});
	
	function reloadCourse(){		
		var keyWord = encodeURIComponent(encodeURIComponent($.trim($("#globalKeyword").val())));
		location.href=CONTEXTPATH+'/portal/frontCourseIndex/course.mooc?keyWord='+keyWord;
	}
})